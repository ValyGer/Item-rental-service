DROP TABLE IF EXISTS users, items, requests, bookings, comments CASCADE;

CREATE TABLE IF NOT EXISTS users (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  name VARCHAR(255) NOT NULL,
  email VARCHAR(512) NOT NULL,
  CONSTRAINT pk_user PRIMARY KEY (id)
  CONSTRAINT uq_user_email UNIQUE (email)
);

CREATE TABLE IF NOT EXISTS requests (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    description VARCHAR(2048) NOT NULL,
    requester_id BIGINT NOT NULL,
    created TIMESTAMP WITHOUT TIME ZONE NOT NULL,
    CONSTRAINT pk_request PRIMARY KEY(id),
    CONSTRAINT fk_request_users FOREIGN KEY(requester_id) REFERENCES users ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS items (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  name VARCHAR(255) NOT NULL,
  description VARCHAR(2048) NOT NULL,
  owner_id BIGINT NOT NULL,
  status BOOLEAN,
  request_id BIGINT,
  CONSTRAINT pk_item PRIMARY KEY (id),
  CONSTRAINT fk_item_users FOREIGN KEY(owner_id) REFERENCES users ON DELETE CASCADE,
  CONSTRAINT fk_item_requests FOREIGN KEY(request_id) REFERENCES requests ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS bookings (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    start_date_time TIMESTAMP WITHOUT TIME ZONE NOT NULL,
    end_date_time TIMESTAMP WITHOUT TIME ZONE NOT NULL,
    item_id BIGINT,
    booker_id BIGINT,
    status varchar(50),
    CONSTRAINT pk_booking PRIMARY KEY(id),
    CONSTRAINT fk_booking_users FOREIGN KEY(booker_id) REFERENCES users ON DELETE CASCADE,
    CONSTRAINT fk_booking_item FOREIGN KEY(item_id) REFERENCES items ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS comments (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    text VARCHAR(2048) NOT NULL,
    item_id BIGINT,
    author_id BIGINT,
    created TIMESTAMP WITHOUT TIME ZONE NOT NULL,
    CONSTRAINT pk_comment PRIMARY KEY(id),
    CONSTRAINT fk_comment_users FOREIGN KEY(author_id) REFERENCES users ON DELETE CASCADE,
    CONSTRAINT fk_comment_items FOREIGN KEY(item_id) REFERENCES items ON DELETE CASCADE
);